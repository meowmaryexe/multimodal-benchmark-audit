# This script runs Qwen2-VL on ONE ChartQA example

from datasets import load_dataset
from transformers import Qwen2VLForConditionalGeneration, AutoProcessor
from qwen_vl_utils import process_vision_info
import torch

# -----------------------------
# Step 1: Load dataset example
# -----------------------------

dataset = load_dataset("lmms-lab/ChartQA", split="test")
example = dataset[0]

question = example["question"]
gold_answer = example["answer"]
image = example["image"]

print("Question:", question)
print("Gold answer:", gold_answer)
print("Image size:", image.size)

# -----------------------------
# Step 2: Load model + processor
# -----------------------------

model_name = "Qwen/Qwen2-VL-2B-Instruct"

processor = AutoProcessor.from_pretrained(model_name)

model = Qwen2VLForConditionalGeneration.from_pretrained(
    model_name,
    torch_dtype=torch.float32,   # safe for CPU
    device_map="auto",           # lets PyTorch choose CPU
)

# -----------------------------
# Step 3: Format input (IMPORTANT)
# -----------------------------

# Qwen expects a chat-style input format
messages = [
    {
        "role": "user",
        "content": [
            {"type": "image", "image": image},  # image input
            {"type": "text", "text": f"{question}\nAnswer with only the final answer. Do not explain."},
        ],
    }
]

# Convert into model-ready text format
text = processor.apply_chat_template(
    messages,
    tokenize=False,
    add_generation_prompt=True,
)

# Extract image inputs in correct format
image_inputs, video_inputs = process_vision_info(messages)

# Tokenize everything together
inputs = processor(
    text=[text],
    images=image_inputs,
    videos=video_inputs,
    padding=True,
    return_tensors="pt",
)

# Move inputs to model device (CPU in your case)
inputs = inputs.to(model.device)

# -----------------------------
# Step 4: Generate answer
# -----------------------------

# Ask the model to generate an answer.
# The output includes BOTH the original prompt tokens and the new answer tokens.
generated_ids = model.generate(
    **inputs,
    max_new_tokens=32,
)

# Remove the original prompt tokens.
# This keeps only the new tokens generated by the model.
generated_ids_trimmed = [
    output_ids[len(input_ids):]
    for input_ids, output_ids in zip(inputs.input_ids, generated_ids)
]

# Decode only the model's generated answer into readable text.
output_text = processor.batch_decode(
    generated_ids_trimmed,
    skip_special_tokens=True,
    clean_up_tokenization_spaces=False,
)

print("\nModel answer:")
print(output_text[0])